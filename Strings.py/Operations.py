#arthimetic operations
print('delhi'+" "+'agra')#string concatenation
print('delhi '*3)#string repetition
print('*'*10)#string repetition

#relational operations
print('delhi'>'agra')#true
print('delhi'<'agra')#false
print('delhi'=='agra')#false
print('delhi'!='agra')#true
print('delhi'>'Delhi')#true

#logical operations
print('delhi'>'agra' and 'delhi'<'agra')#false
print('delhi'>'agra' or 'delhi'<'agra')#true
print('hello'or'world')#hello
print('hello'and'world')#world
print('hello'and'')#''
print('hello' or'')#hello
print(not 'hello')#false
print(not '')#true

#loop operations
for i in 'delhi':
    print(i)
for i in 'delhi':
    print("pune")

#membership operations
print('d' in 'delhi')#true
print('D' in 'delhi')#false
print('d' not in 'delhi')#false
print('D' not in 'delhi')#true

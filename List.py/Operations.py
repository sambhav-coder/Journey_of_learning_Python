#operations
#arithmetic operations
L=[1,2,3]
L2=[4,5,6]
print(L+L2)  # Output: [1, 2, 3, 4, 5, 6]
print(L*2)   # Output: [1, 2, 3, 1, 2, 3]   

#membership operations
L=[1,2,3]
print(2 in L)  # Output: True
print(4 not in L)  # Output: True

#loop operations
L=[1,2,3]
for i in L:
    print(i)  # Output: 1 2 3
